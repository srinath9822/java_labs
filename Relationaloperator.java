public class Relationaloperator {

	public static void main(String[] args) {
		int num1=20,num2=15;
		System.out.println("num1= "+num1);
		System.out.println("num2= "+num2);
		System.out.println("num1 > num2 is "+(num1>num2));
		System.out.println("num1 < num2 is "+(num1<num2));
		System.out.println("num1 >= num2 is "+(num1>=num2));
		System.out.println("num1 <= num2 is "+(num1<=num2));
		System.out.println("num1 == num2 is "+(num1==num2));
		System.out.println("num1 != num2 is "+(num1!=num2));

	}

}
